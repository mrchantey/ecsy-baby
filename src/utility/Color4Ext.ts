import { Color4 } from "babylonjs";

export const Red = () => new Color4(1, 0, 0);
export const Green = () => new Color4(0, 1, 0);
export const Blue = () => new Color4(0, 0, 1);
export const Black = () => new Color4(0, 0, 0);
export const White = () => new Color4(1, 1, 1);
export const Purple = () => new Color4(0.5, 0, 0.5);
export const Magenta = () => new Color4(1, 0, 1);
export const Yellow = () => new Color4(1, 1, 0);
export const Gray = () => new Color4(0.5, 0.5, 0.5);
export const Teal = () => new Color4(0, 1.0, 1.0);
export const Random = () => new Color4(Math.random(), Math.random(), Math.random());
